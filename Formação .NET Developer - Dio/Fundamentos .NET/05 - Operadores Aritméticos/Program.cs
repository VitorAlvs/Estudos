// Addition (+)
int a = 5;
int b = 3;
int c = a + b;

Console.WriteLine("Addition");
Console.WriteLine(a + " + " + b + " = " + c);
Console.WriteLine(c);

Console.WriteLine("---------------------");
// Subtraction (-)
int d = 4;
int e = 6;
int f = d - e;

Console.WriteLine("Subtraction");
Console.WriteLine(d + " - " + e + " = " + f);
Console.WriteLine(f);

Console.WriteLine("---------------------");
// Multiplication (*)
int g = 8;
int h = 9;
int i = g * h;

Console.WriteLine("Multiplication");
Console.WriteLine(g + " * " + h + " = " + i);
Console.WriteLine(i);

Console.WriteLine("---------------------");
// Division (\)
int j = 25;
int k = 5;
int l = j/k;

Console.WriteLine("Division");
Console.WriteLine(j + " / " + k + " = " + l);
Console.WriteLine(l);

Console.WriteLine("---------------------");
// Modulus (%)
int m = 21;
int n = 5;
int o = m%n;

Console.WriteLine("Modulus");
Console.WriteLine(m + " % " + n + " = " + o);
Console.WriteLine(o);

Console.WriteLine("---------------------");
// Increment (++)
Console.WriteLine("Increment");
int p = 1;
Console.WriteLine("p = " + p);
Console.WriteLine("p++");
p++;
Console.WriteLine("p = " + p);

Console.WriteLine("---------------------");
// Decrement (--)
Console.WriteLine("Decrement");
int q = 2;
Console.WriteLine("q = " + q);
Console.WriteLine("q--");
q--;
Console.WriteLine("q = " + q);