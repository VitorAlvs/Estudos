#Atribuição simples
saldo = 500
print(saldo)

#Atribuição com adição
saldo = 200
saldo += 500
print(saldo)

#Atribuição com subtração
saldo = 200
saldo -= 500
print(saldo)

#Atribuição com multiplicação
saldo = 200
saldo *= 500
print(saldo)

#Atribuição com divisão
saldo = 200
saldo /= 500
print(saldo)

saldo = 500
saldo //= 5
print(saldo)

#Adição com módulo
saldo = 500
saldo %= 480
print(saldo)

#Adição com exponenciação
saldo = 90
saldo **= 2
print(saldo)